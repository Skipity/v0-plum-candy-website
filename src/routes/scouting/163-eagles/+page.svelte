					<p>
						Of any one hundred scouts who join, it is true that thirty may step away within their first year. Life has a way of calling them down different paths. But the promise of Scouting, once made, is never truly lost. The smell of a campfire, the weight of a backpack, the quiet echo of the Scout Oath, these things leave a lasting mark. Years later, they will remember the lessons and the laughter. They will smile with a distant pride and say, "I was a Scout," knowing that brief experience helped shape them for the better.
					</p>
					
					<p>
						Of those one hundred scouts, only rarely will one find himself on the wrong side of the law. Scouting gives a boy an inner compass, a moral code to live by, a promise to be trustworthy, loyal, and kind. For twelve who come from families with no church home, Scouting will open a door to faith, building a spiritual foundation for them and their loved ones that will last a lifetime. Six will feel a higher calling so deeply that they will enter the clergy, dedicating their lives to serving others.
					</p>
					
					<p>
						Every single one of those one hundred scouts will learn that his own two hands are powerful tools. He learns the quiet confidence that comes from knowing how to tie a knot that holds, read a map that leads, and build a fire that gives warmth. Almost all of them will develop hobbies that bring joy and wonder to their lives. Nearly half will go on to serve our country in the military, their Scout training a bedrock of their readiness. At least one will use his skills in a sudden moment of crisis to save a life, and many more will credit Scouting with saving their own.
					</p>
					
					<p>
						Out of the one hundred, four will climb the final summit to reach the rank of Eagle Scout. It is a journey that will ask for everything they have and give back more than they could ever imagine. One will later say that he values his Eagle medal even more than his college degree, because it represents not what he learned, but who he became. Along the path, seventeen will be so moved by their journey that they will return as Scout leaders. They will pay it forward, giving their time and wisdom to guide thousands of new scouts who follow.
					</p>
					
