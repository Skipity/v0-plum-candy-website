						This is the story of our one hundred scouts, but their legacy ripples far beyond the troop meeting. While only one in four boys in this country will ever become a Scout, look closely at our nation's leaders. Look at the people who innovate in business, who serve in our communities, and who guide our country. You will find that three out of four of them once wore the uniform. This story will never truly end. It is a promise of character, passed from one heart to the next, a timeless legacy ensuring that good people will always be ready to stand up, to help out, and to lead the way.
